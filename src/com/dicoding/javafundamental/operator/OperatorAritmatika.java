package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {
    public  static void main(String[] args){
        System.out.println("Operasi penjumlahan");
        int hasilPenjumlahan = 5 + 3;
        System.out.println("Hasil Penjumlahan 5 + 3 adalah : "+hasilPenjumlahan);

        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 5 - 3;
        System.out.println("Hasil pengurangan 5 - 3 adalah :"+ hasilPengurangan);

        System.out.println("Operasi Perkalian");
        int hasilPerkalian = 5 * 3;
        System.out.println("Hasil perkalian dari 5 x 3 adalah :"+ hasilPerkalian);

        System.out.println("Operasi Pembagian");
        int hasilPembagian = 15 / 3;
        System.out.println("Hasil Bagi 15 / 3 adalah :"+ hasilPembagian);

    }
}
